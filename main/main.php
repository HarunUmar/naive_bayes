<p> 
<b>Sistem pakar (<i>expert system</i>) </b>adalah sistem yang berusaha mengapdosi pengetahuan manusia ke komputer, agar komputer dapat menyelesaikan masalah seperti yang biasa dilakukan oleh para ahli. Sistem pakar yang baik dirancang agar dapat menyelesaikan suatu permasalahan tertentu dengan meniru kerja dari para ahli.
</p><br/>
 
<p>Setiap  penderita yang datang berobat pada seorang dokter mendambakan diperiksa sebaik mungkin agar dapat diketahui penyakitnya dengan jelas untuk kemudian dapat diberikan obat yang setepat-tepatnya dengan harapan agar dapat smbuh kembali dalam waktu yang sesingkat-singkatnya dan dengan biaya serendah-rendahnya. Dambaan pendeita ini adalah suatu kewajiban moral di samping kewajiban profesi bagi seoran gdokter, maka adalah wajar dan hal ini juga menjadi harapan dari pembuatan sistem ini.
</p><br>
<br />
Tuberkulosis atau disingkat TB merupakan penyakit yang disebabkan oleh infeksi yang menyerang jaringan paru-paru. Penyebab seseorang mengidap TB adalah bakteri mycobacterium tuberculosis. Sebagian besar orang memiliki mikroba TB di dalam tubuhnya, tapi mikroba ini hanya menyebabkan penyakit di beberapa orang saja, biasanya jika imunitas atau kekebalan tubuh orang itu menurun.</p>
